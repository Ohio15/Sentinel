import React, { useState, useEffect } from 'react';

interface SidebarProps {
  currentPage: string;
  onNavigate: (page: any) => void;
}

export function Sidebar({ currentPage, onNavigate }: SidebarProps) {
  const [version, setVersion] = useState<string>('');

  useEffect(() => {
    window.api.updater.getVersion().then(setVersion);
  }, []);
  const menuItems = [
    { id: 'dashboard', label: 'Dashboard', icon: DashboardIcon },
    { id: 'devices', label: 'Devices', icon: DevicesIcon },
    { id: 'tickets', label: 'Tickets', icon: TicketsIcon },
    { id: 'alerts', label: 'Alerts', icon: AlertsIcon },
    { id: 'scripts', label: 'Scripts', icon: ScriptsIcon },
    { id: 'settings', label: 'Settings', icon: SettingsIcon },
  ];

  return (
    <aside className="w-64 bg-surface border-r border-border flex flex-col">
      {/* Logo */}
      <div className="p-6 border-b border-border">
        <div className="flex items-center gap-3">
          <div className="w-10 h-10 bg-primary rounded-lg flex items-center justify-center">
            <ShieldIcon className="w-6 h-6 text-white" />
          </div>
          <div>
            <h1 className="text-lg font-bold text-text-primary">Sentinel</h1>
            <p className="text-xs text-text-secondary">RMM Platform</p>
          </div>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4">
        <ul className="space-y-1">
          {menuItems.map((item) => (
            <li key={item.id}>
              <button
                onClick={() => onNavigate(item.id)}
                className={`w-full flex items-center gap-3 px-4 py-3 rounded-lg transition-colors ${
                  currentPage === item.id || (currentPage === 'device-detail' && item.id === 'devices') || (currentPage === 'ticket-detail' && item.id === 'tickets')
                    ? 'bg-primary-light text-primary'
                    : 'text-text-secondary hover:bg-gray-100'
                }`}
              >
                <item.icon className="w-5 h-5" />
                <span className="font-medium">{item.label}</span>
              </button>
            </li>
          ))}
        </ul>
      </nav>

      {/* Footer */}
      <div className="p-4 border-t border-border">
        <div className="text-xs text-text-secondary text-center">
          Version {version}
        </div>
      </div>
    </aside>
  );
}

